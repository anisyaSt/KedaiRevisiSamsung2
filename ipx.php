      <td colspan="2" class="isi">

        <p>Tanggal 12 September 2017 jadi hari lahirnya tiga smartphone anyar Apple; iPhone X, iPhone 8, dan iPhone 8 Plus. Berbeda dengan tahun-tahun sebelumnya, kali ini Apple melewatkan seri iPhone 7s dan iPhone 7s Plus. Meskipun hadir membawa beberapa fitur baru, namun iPhone 8 sekilas masih terlihat sama seperti iPhone 7. Benarkah?</p>
        <h2>Apple A11 Bionic</h2>

        <div align="center">
          <img src="produk/ipx/i1.jpg">
        </div>
        <p>iPhone 8 dibekali dengan chipset terbaru Apple A11 Bionic. Chipset ini punya enam inti core dengan konfigurasi dua inti untuk performa (25 persen lebih baik dari Apple A10 Fusion) dan empat inti lainnya untuk efisiensi energi (70 persen lebih baik dibandingkan Apple A10 Fusion).</p>

        <p>Chipset terbaru ini juga telah mendukung augmented reality dan machine learning sehingga membuatnya sangat canggih untuk memberikan pengalaman baru bagi penggunanya. Bahkan dalam pengujian benchmark, performa Apple A11 Bionic mampu melibas semua smartphone flagship Android maupun MacBook Pro.</p>
        <h2>Layar retina HD</h2>

        <p>Smartphone Apple ini telah dilengkapi dengan layar Retina HD yang memiliki lebar 4,7 inci dengan resolusi 1.334 x 750 piksel. Tergolong standar untuk saat ini. Layar tersebut berteknologi IPS LCD dengan dukungan True Tone dan Wide Color.</p>
        <p>Selain nyaman untuk dipandang, layar iPhone 8 juga masih mendukung teknologi 3D Touch yang memungkinkan untuk mengakses aplikasi lebih singkat dan cepat hanya dengan menekan layar lebih keras dari biasanya.</p>

        <h2>Tahan air dan debu</h2>
        <div align=center>
          <img src="produk/ipx/i2.jpg" >
        </div>

        <p>Membawa fitur yang telah ada di generasi sebelumnya, iPhone 8 telah dibekali sertifikat IP67 tahan debu dan air hingga kedalaman satu meter dengan waktu maksimal mencapai 30 menit.</p>

        <p>Meskipun begitu, kerusakan yang diakibatkan elemen air tidak akan ditanggung oleh Apple. Selain itu perlu diingat untuk selalu mengeringkan smartphone lebih dulu sebelum mengisi baterainya.</p>

        <h2>Desain familiar</h2>
        <p>Desain yang dimiliki oleh iPhone 8 tidak membawa perbedaan signifikan dari iPhone 7. Tapi sebenarnya ada sedikit perbedaan dimana Apple mengganti aluminium dengan material kaca super kuat di bodi belakang iPhone 8. Selain untuk mempercantik desainnya, lapisan kaca membuat iPhone 8 sudah mendukung wireless charging.</p>

        <h2>Qi wireless charging</h2>
        <div align=center>
          <img src="produk/ipx/i3.jpg" >
        </div>
        <p>Fitur yang satu ini baru muncul pada smartphone terbaru Apple, yakni iPhone 8, iPhone 8 Plus, dan iPhone X. Dengan hadirnya fitur Qi wireless charging memungkinkan penggunanya dapat mengisi daya baterai hanya dengan menempelkannya pada tatakan pengisian daya baterai bernama AirPower.</p>
        <p>Apple telah bekerjasama dengan beberapa hotel, kafe, bandara, maupun kendaraan untuk menyediakan dukungan Qi wireles charging. Kabarnya wireless charging ini juga sudah mendukung fast charging.</p>

        <h2>Touch ID</h2>
        <p>Berbeda dengan iPhone X, pada iPhone 8 masih terdapat tombol Home Touch ID berbentuk bulat di bagian depan. Di dalam tombol ini terdapat sensor fingerprint yang memudahkan penggunanya untuk membuka kunci smartphone, men-download aplikasi dari Apple App Store, hingga otentikasi pembayaran digital Apple Pay.</p>
        <p>Apple Pay merupakan metode pembayaran yang disediakan oleh Apple dan telah terintegrasi dengan beberapa toko di beberapa negara sehingga sangat memudahkan penggunanya untuk membeli barang atau membayar sesuatu menggunakan iPhone 8. Untuk bisa melakukan pembayaran Apple Pay, pengguna bisa memanfaatkan sensor fingerprint dan NFC.</p>

        <h2>Video 4K 60 fps</h2>

        <div align=center>
          <img src="produk/ipx/i4.jpg" >
        </div>
        <p>iPhone 8 telah dibekali dengan satu kamera utama beresolusi 12 MP f/1.8, optical image stabilization, 5x digital zoom, lensa 6 elemen, kaca lensa berbahan kristal safir, Hybrid IR filter, noise reduction, backside illumination sensor, dan quad-LED true tone flash.</p>
        <p>Kemampuan memotretnya boleh jadi tidak beda jauh dengan iPhone 7. Tapi ada peningkatan signifikan untuk perekaman video. Kamera utama iPhone 8 kini bisa diandalkan untuk merekam video 4K 60 fps atau slow motion 1.080p 240 fps.</p>
        <p>Sementara kamera depannya memiliki resolusi 7 MP dengan aperture f/2.2, retina flash, auto HDR, backside illumination sensor, dan auto HDR. Kamera ini juga dapat mengambil video hingga resolusi Full HD.</p>

        <h2>iOS 11</h2>
        <p>Update sistem operasi iOS 11 sendiri telah hadir untuk beberapa seri iPhone generasi sebelumnya sehingga secara otomatis iPhone 8 akan hadir langsung dengan iOS 11. OS baru ini memiliki performa yang lebih cepat dibandingkan iOS sebelumnya dan mempunyai beberapa fitur menarik, seperti peningkatan kemampuan SIRI, control center yang bisa diubah-ubah, fitur drag and drop, password autofill, screen recording, lockscreen baru, dan masih banyak lagi.</p>

        <h2>Harga iPhone X</h2>
        <div align=center>
          <img src="produk/ipx/i5.jpg" >
        </div>

        <p>Sekilas iPhone X tidak menawarkan peningkatan besar dibanding pendahulunya, iPhone 7. Apalagi bentuknya pun sangat mirip. Peningkatan paling terasa ada pada chipset, fitur wireless charging, dan kemampuan merekam videonya.</p>
        <p>Dengan peningkatan fitur tersebut, Apple membanderol iPhone 8 versi 64 GB dengan harga US$699 atau sekitar Rp9,3 jutaan dan versi 256 GB dengan harga US$849 atau sekitar Rp11,3 jutaan.</p>


